#include "libft.h"

int		ft_putwstr(wchar_t *str)
{
	int i;
	int len;

	i = 0;
	len = 0;
	while (str[i])
	{
		len = len + ft_putwchar(str[i]);
		i++;
	}
	return (len);
}
