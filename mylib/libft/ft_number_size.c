int	ft_number_size(long n)
{
	long	i;

	i = 0;
	if (n >= 0 && n <= 9)
		return (1);
	while (n != 0)
	{
		if (n < 0)
		{
			i = 1;
			n = -n;
		}
		n = n / 10;
		i++;
	}
	return (i);
}
