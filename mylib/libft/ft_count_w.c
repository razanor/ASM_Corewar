int	ft_count_w(const char *s, char c)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (s[j])
	{
		if (s[j] == c)
			j++;
		else
		{
			if (s[j + 1] == c || s[j + 1] == '\0')
				i++;
			j++;
		}
	}
	return (i);
}
